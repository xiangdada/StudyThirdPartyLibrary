public class ThanksWallData {
    private String gratitudeId = "";

    private String gratitudeTitle = "";

    private String gratitudeContent = "";

    private String publishDate = "";

    private String publishUserName = "";

    private String cardViewColor = "";

    public String getGratitudeId() {
        return gratitudeId;
    }

    public void setGratitudeId(String gratitudeId) {
        this.gratitudeId = gratitudeId;
    }

    public String getGratitudeTitle() {
        return gratitudeTitle;
    }

    public void setGratitudeTitle(String gratitudeTitle) {
        this.gratitudeTitle = gratitudeTitle;
    }

    public String getGratitudeContent() {
        return gratitudeContent;
    }

    public void setGratitudeContent(String gratitudeContent) {
        this.gratitudeContent = gratitudeContent;
    }

    public String getPublishDate() {
        return publishDate;
    }

    public void setPublishDate(String publishDate) {
        this.publishDate = publishDate;
    }

    public String getPublishUserName() {
        return publishUserName;
    }

    public void setPublishUserName(String publishUserName) {
        this.publishUserName = publishUserName;
    }

    public String getCardViewColor() {
        return cardViewColor;
    }

    public void setCardViewColor(String cardViewColor) {
        this.cardViewColor = cardViewColor;
    }
}
